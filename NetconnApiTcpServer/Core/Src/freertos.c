/* USER CODE BEGIN PM */
void phy_check(void) {
    uint32_t phy_reg_value = 0;
    uint32_t target_reg = 0x02; // Thanh ghi PHY ID

    for (uint32_t addr = 0; addr < 32; addr++) {
        if (HAL_ETH_ReadPHYRegister(&heth, addr, target_reg, &phy_reg_value) == HAL_OK) {
            if (phy_reg_value != 0xFFFF && phy_reg_value != 0x0000) {
                printf("INFO: PHY found at Address: %ld (RegVal: 0x%lX)\r\n", addr, phy_reg_value);
                HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12 | GPIO_PIN_13 | GPIO_PIN_14 | GPIO_PIN_15, GPIO_PIN_SET);
                 break;
            }
        }
    }
}
